// Operator types in js


// 1. Arithmetic operators: + - / % * ++ --
var x=10;
var y =20;

console.log(x+y);

console.log(x-y);

console.log(x%y);   

console.log(x/y);

console.log(x*y);

//increment: two types:
// 1)post: x++ firstly returns the current value then  elemnt 
// 2) pre: ++x: firstly increment then give u the current value
console.log(x++);
console.log(++x);

//decrement: two types:
// 1)post: x-- firstly returns the current value then  decrement
// 2) pre: --x: firstly decrement then give u the current value

console.log(x--);
console.log(--x);



// 2. Comparison operators:  == ===  != !== > >= < <=


